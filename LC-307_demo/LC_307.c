static void SensorConfig_UartSend(uint8_t data)
{
    IoTUartWrite(LC_307_UART_IDX,&data,1);
}

uint8_t LC_307_Init()
{
    //初始化 IO 口
    IoTGpioInit(LC_307_UART_RX_PIN);
    IoTGpioSetFunc(LC_307_UART_RX_PIN,HI_IO_FUNC_GPIO_1_UART1_RXD);

    IoTGpioInit(LC_307_UART_TX_PIN);
    IoTGpioSetFunc(LC_307_UART_TX_PIN,HI_IO_FUNC_GPIO_0_UART1_TXD);

    //初始化串口
    IotUartAttribute g_uart_cfg = {LC_307_UART_BAUD, 8, 1, IOT_UART_PARITY_NONE, 500, 500, 0};
    int ret = IoTUartInit(LC_307_UART_IDX, &g_uart_cfg);
    if (ret != 0) 
    {
        printf("uart init fail\r\n");
        return ret;
    }

    osDelay(10);

    uint16_t i;
    uint16_t len ;
    uint8_t recv[3];
    int recv_cnt;		
    len = sizeof(Sensor_cfg);	
    SensorConfig_UartSend(0xAA);//0xAA指令
    SensorConfig_UartSend(0xAB);//0xAB指令		 
    SensorConfig_UartSend(tab_focus[0]);		
    SensorConfig_UartSend(tab_focus[1]);
    SensorConfig_UartSend(tab_focus[2]);
    SensorConfig_UartSend(tab_focus[3]);
    SensorConfig_UartSend(tab_focus[0]^tab_focus[1]^tab_focus[2]^tab_focus[3]);			 		 
    recv_cnt = 0;

    IoTUartRead(LC_307_UART_IDX,recv,3);

    if(((recv[0]^recv[1]) == recv[2]) & (recv[1] == 0x00)) 
    {
        printf("AB Command configuration successconfig succefful\n");
        recv[0] = 0; recv[1] = 0; recv[2] = 0;
    }		
    else{
        printf("AB Command configuration successconfig failed\n");
        return 1;
    }
    
    for(i=0;i<len;i+=2)//0xBB? 噶?
    {
        SensorConfig_UartSend(0xBB);		 
        SensorConfig_UartSend(SENSOR_IIC_ADDR);		
        SensorConfig_UartSend(Sensor_cfg[i]);
        SensorConfig_UartSend(Sensor_cfg[i+1]);
        SensorConfig_UartSend(SENSOR_IIC_ADDR^Sensor_cfg[i]^Sensor_cfg[i+1]);	 
        recv_cnt = 0;

        IoTUartRead(LC_307_UART_IDX,recv,3);

        if(((recv[0]^recv[1]) == recv[2]) & (recv[1] == 0x00))
            printf("config_num:%d BB Command configuration successconfig succefful\n",i);
        else
            printf("config_num:%d BB Command configuration successconfig succefful\n",i);								 
    }	 		 
    SensorConfig_UartSend(0xDD);//0xDD		
    printf("Configuration success\n");

    osDelay(50);

    return 1;
}

void LC_307_Get_Data(LC_307_Data *Data_Out)
{   
    IoTUartRead(LC_307_UART_IDX, buffer ,LC_307_DATA_LENGHTH);

    if(buffer[0] == 0xFE && buffer[1] == 0x0A && buffer[13] == 0x55)
    {
        uint8_t xor = buffer[2]^buffer[3]^buffer[4]^buffer[5]^buffer[6]^buffer[7]^buffer[8]^buffer[9]^buffer[10]^buffer[11];
        if(buffer[10] == 0xF5 && buffer[12] == xor)
        {
            Data_Out->version = buffer[11];
            Data_Out->Vx = ((buffer[3]<<8)|buffer[2]);
            Data_Out->Vy = ((buffer[5]<<8)|buffer[4]);
            Data_Out->dt = ((buffer[7]<<8)|buffer[6]);
            Data_Out->Groung_H = ((buffer[9]<<8)|buffer[8]);
        }
        else
        {
            printf("LC_307_Get_Data failed! the Data is not valid!\n");
        }
    }
    else
    {
        printf("LC_307_Get_Data failed! the buffer is not right!\n");
    }

}